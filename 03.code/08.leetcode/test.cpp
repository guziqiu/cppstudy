struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

int main() {
	TreeNode *root = new TreeNode(3);
	root->left = new TreeNode(9);
	root->right = new TreeNode(20);
	root->right->left = new TreeNode(15);
	root->right->right = new TreeNode(7);

	queue<TreeNode *> q;
	q.push(root);

	int arr[] = {1, 2, 3, 4};
	int *a = arr;
	queue<int *> que;
	que.push(a);
	cout << q.size() << endl;

	return 0;
}
